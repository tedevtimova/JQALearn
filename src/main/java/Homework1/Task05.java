package Homework1;

public class Task05 {

    public static void main(String[] args) {

        for(int num=1;num<=100;num++){
            System.out.println(num);
        }

        System.out.println("Next for cycle starts now:");

        for(int num=100;num>=1;num--){
            System.out.println(num);
        }

    }

}

